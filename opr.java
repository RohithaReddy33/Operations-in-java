public class opr {
    public static void main (String[]args){
        int a = 40;
        int b = 50;
        boolean c=true;
        boolean d=false;
//ARTHEMATIC OPERATIONS ON INTEGER DATA TYPE
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
//RELATIONAL OPERATION:
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);
//LOGICAL OPERATION:
        System.out.println(c&&d);
        System.out.println(c||d);
    }
}